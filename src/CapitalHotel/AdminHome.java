    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nameoremail=jTextField1.getText();
        ResultSet rs=Select.getSelect("select *from user where name='"+nameoremail+"'or email='"+nameoremail+"'");
        DefaultTableModel dtm=(DefaultTableModel)jTable1.getModel();
        
        
        if(jTextField1.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"enter name or email");
        }
        else
        {
        try
        {
            if(rs.next())
            {
                dtm.setRowCount(0);
                dtm.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(6),rs.getString(7)});
                jTextField1.setText("");
            }
            else
            {
                JOptionPane.showMessageDialog(this, "No user found in this email or name");
                jTextField1.setText("");
            }
            rs.close();
            
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e);
        }
        
     
        
        }
    }//GEN-LAST:event_jButton1ActionPerformed


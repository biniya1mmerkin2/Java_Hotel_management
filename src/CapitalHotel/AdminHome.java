    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int i=jTable1.getSelectedRow();
        TableModel tm=jTable1.getModel();
        String name=tm.getValueAt(i,0).toString();
        String states=tm.getValueAt(i,5).toString();
        
        if(states=="true")
        {
            states="false";
            int j=JOptionPane.showConfirmDialog(this,"Do you want to remove the Approval?","select",JOptionPane.OK_OPTION);
            if(j==0)
            {
                tm.setValueAt(states,i,5);
                String query="update user set states='false' where name='"+name+"'";
                InsertinTo.setData(query,"");
            }
        }
        else
        {
            states="true";
            int j=JOptionPane.showConfirmDialog(this,"Do you want to give  Approval?","select",JOptionPane.OK_OPTION);
            if(j==0)
            {
                tm.setValueAt(states,i,5);
                String query="update user set states='true' where name='"+name+"'";
                InsertinTo.setData(query,"");
            }
        }
        
    }//GEN-LAST:event_jTable1MouseClicked

